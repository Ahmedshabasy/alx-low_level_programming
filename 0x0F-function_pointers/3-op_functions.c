#include "3-calc.h"

/**
 * op_add - add numbers
 * @a: first
 * @b: second
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub numbers
 * @a: first
 * @b: second
 *
 * Return: result
 */

int op_sub(int a, int b)
{
        return (a - b);
}

/**
 * op_mul - multiplie numbers
 * @a: first
 * @b: second
 *
 * Return: result
 */

int op_mul(int a, int b)
{
        return (a * b);
}

/**
 * op_div - division numbers
 * @a: first
 * @b: second
 *
 * Return: result
 */

int op_div(int a, int b)
{
        return (a / b);
}

/**
 * op_mod - module two numbers
 * @a: first
 * @b: second
 *
 * Return: result
 */

int op_mod(int a, int b)
{
        return (a % b);
}
